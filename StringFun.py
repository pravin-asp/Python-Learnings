# startswith
# endswith

s = 'python is easy'
print(s.startswith('python')) # sentence starts with the word
print(s.endswith('python')) ## sentence ends with the word

print(s.isalpha()) # checks it is only alphabets
print(s.isdigit()) # checks it is only digits

print(s.islower()) # checks it is lower
print(s.isupper()) # checks it is upper

print('Python Is Easy'.istitle())
print(' '.isspace())