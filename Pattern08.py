'''
- - - - -
- - - -
- - -
- -
-
'''
row = 5
while row >= 1:
	col = 1
	while col <= row:
		print('-', end = ' ')
		col += 1
	print()
	row -= 1
	
'''
- - - - - 1
- - - - 1
- - - 1
- - 1
- 1
'''
row = 5
while row >= 1:
	col = 1
	while col <= row:
		print('-', end = ' ')
		col += 1
	print(1)
	row -= 1

